#!/usr/bin/env python3
"""
25_resolve_corrupted_names.py — Resolve encoding-corrupted and orphan unresolved placements.

Investigated 30 remaining full-name unresolved placements. Grouped into five work items:

  A) UUID_BACKFILL   — person already in Truth but PBP row has no UUID.
                       (Jocelyn Sandoval + Steven Sevilla were SKIPped by tool 24
                       because they were already in Truth, but their PBP UUID was
                       never assigned.)

  B) TRUTH_PROMOTE   — UUID exists in PBP but person absent from Truth (orphan).
                       Ian Pfeiffer: 2 PBP rows with UUID 0680f071, not in Truth.

  C) NEW_PERSONS     — No UUID anywhere. Generate deterministic UUID, create Truth
                       row, assign UUID to PBP rows.
                       Matjaž Borič: corrupted as "Matja? Borič" in PBP.

  D) CANON_CORRECT   — Encoding-corrupted person_canon in PBP. Rename to clean
                       canon and assign correct UUID.

  E) NOISE           — Entries that are not real persons.
                       Two broad sub-groups:
                       1. Doubles pairs stored as singles with corrupted "?" separator
                          (the "?" replaced "&" or "/" between two partner names).
                          Both persons in the pair already appear in Truth under their
                          individual canons. The concatenated entry has no clean mapping
                          and is structurally a mis-parsed team.
                       2. Non-person text (Czech division description).

Inputs:
  inputs/identity_lock/Persons_Truth_Final_v24.csv
  inputs/identity_lock/Persons_Unresolved_Organized_v21.csv
  inputs/identity_lock/Placements_ByPerson_v25.csv

Outputs (with --apply):
  inputs/identity_lock/Persons_Truth_Final_v25.csv
  inputs/identity_lock/Persons_Unresolved_Organized_v22.csv  (unchanged copy)
  inputs/identity_lock/Placements_ByPerson_v26.csv
"""

from __future__ import annotations

import argparse
import sys
import uuid
from pathlib import Path

import pandas as pd

ROOT = Path(__file__).resolve().parent.parent
IDENTITY_LOCK = ROOT / "inputs" / "identity_lock"

TRUTH_IN       = IDENTITY_LOCK / "Persons_Truth_Final_v24.csv"
UNRESOLVED_IN  = IDENTITY_LOCK / "Persons_Unresolved_Organized_v21.csv"
PLACEMENTS_IN  = IDENTITY_LOCK / "Placements_ByPerson_v25.csv"

TRUTH_OUT      = IDENTITY_LOCK / "Persons_Truth_Final_v25.csv"
UNRESOLVED_OUT = IDENTITY_LOCK / "Persons_Unresolved_Organized_v22.csv"
PLACEMENTS_OUT = IDENTITY_LOCK / "Placements_ByPerson_v26.csv"

# ---------------------------------------------------------------------------
# Deterministic UUID generation (project-specific namespace)
# ---------------------------------------------------------------------------
_NS = uuid.uuid5(uuid.NAMESPACE_URL, "footbag-results-identity")

def make_uuid(canon: str) -> str:
    return str(uuid.uuid5(_NS, canon))


# ---------------------------------------------------------------------------
# A) UUID_BACKFILL
#    person_canon is in Truth but PBP row(s) have empty person_id.
#    {person_canon: truth_uuid}
# ---------------------------------------------------------------------------
UUID_BACKFILL: dict[str, str] = {
    # Jocelyn Sandoval and Steven Sevilla were already in Truth when tool 24 ran,
    # so tool 24 SKIPped them in NEW_PERSONS — but never assigned their UUID in PBP.
    "Jocelyn Sandoval": "1cbaa989-68df-5416-984a-564f619af31f",
    "Steven Sevilla":   "b5aa549c-480b-5457-993e-221d86a12e51",
}

# ---------------------------------------------------------------------------
# B) TRUTH_PROMOTE
#    UUID exists in PBP but person absent from Truth (orphan).
#    {person_canon: pbp_uuid}
# ---------------------------------------------------------------------------
TRUTH_PROMOTE: dict[str, str] = {
    "Ian Pfeiffer": "0680f071-3207-5a8e-88e5-32b4ecfe24cd",
}

# ---------------------------------------------------------------------------
# C) NEW_PERSONS
#    No UUID anywhere. UUID generated deterministically.
# ---------------------------------------------------------------------------
NEW_PERSONS: list[str] = [
    "Matjaž Borič",   # 2008 Intermediate Singles Routines; "Matja? Borič" in PBP
]

# Pre-computed for reference (generated by make_uuid):
# "Matjaž Borič" → 33314117-41ab-5fa6-adf6-998eeefba8aa

# ---------------------------------------------------------------------------
# D) CANON_CORRECT
#    Encoding-corrupted person_canon in PBP → clean_canon.
#    UUID resolved at runtime from Truth (pre-existing or added in B/C).
# ---------------------------------------------------------------------------
CANON_CORRECT: dict[str, str] = {
    # corrupted_in_pbp → clean_canon (UUID resolved at runtime)
    "Ian Pfeiffer ?": "Ian Pfeiffer",   # trailing ? = corrupted char after name
    "Matja? Borič":   "Matjaž Borič",   # ž corrupted to ?
}

# ---------------------------------------------------------------------------
# E) NOISE
#    Not real persons. Reclassify person_canon → __NON_PERSON__ in PBP.
#
#    Sub-group 1: Doubles pairs concatenated with " ? " separator.
#    The "?" replaced a corrupted separator character (&, /, or similar).
#    Both partners in each pair already appear in Truth under their
#    individual canons and have their own correct PBP rows elsewhere.
#    These concatenated entries are structurally mis-parsed teams stored
#    as singles — no clean person identity is possible.
#
#    Sub-group 2: Non-person text.
# ---------------------------------------------------------------------------
NOISE: set[str] = {
    # Sub-group 1: corrupted doubles pairs (separator "?" between two names)
    "Andreas Wolff ? Uli Haase",               # 2009 Open Doubles Net
    "Kerstin Anhuth ? Chris Löw",              # 2009 Open Mixed Doubles Net
    "Lisa Uebele ? Andreas Wolff",             # 2009 Open Mixed Doubles Net
    "Lisa Uebele ? Kerstin Anhuth",            # 2009 Open Doubles Net (2 rows)
    "Lino Landau ? Marc Brunet",               # 2007 Doubles Net
    "Ludovic Lacaze ? Eric Fonteneau",         # 2007 Doubles Net
    "Manuel Kruse ? Simon Voss",               # 2009 Open Doubles Net
    "Nicolas de Zeeuw ? Miquel Clemente",      # 2007 Doubles Net (Nicolas De Zeeuw + Miquel Clemente both in Truth)
    "Ninni Liukko ? Piia Tantarimäki",         # 2009 Open Doubles Net (2 rows)
    "Ninni Liukko ? Yassin Khateeb",           # 2009 Open Mixed Doubles Net
    "Petr Stejskal ? Pavel Cerveny",           # 2009 Intermediate Doubles Net
    "Piia Tantarimäki ? Uli Haase",            # 2009 Open Mixed Doubles Net
    "Yassin Khateeb ? Markus Portenkirchner",  # 2009 Open Doubles Net
    "Michał Róg ? Michał Klimczak",            # 2009 Intermediate Doubles Net
    # Apostrophe corrupted to ?, then concatenated with partner via hyphen:
    "Dominic O?Brien-Stéphane Tailleur",       # 2007 Open Doubles (O'Brien + Tailleur both in Truth)
    # Sub-group 2: non-person text
    "tivteřinová smr?ť triků",                 # Czech: "X-second trick death" — division descriptor, not a person
}


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def build_truth_row(cols: list[str], pid: str, canon: str, source: str) -> dict:
    row = {c: "" for c in cols}
    row["effective_person_id"] = pid
    row["person_canon"] = canon
    row["player_ids_seen"] = pid
    row["player_names_seen"] = canon
    row["source"] = source
    row["norm_key"] = canon.lower().strip()
    if "person_canon.1" in row:
        row["person_canon.1"] = canon
    return row


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> int:
    ap = argparse.ArgumentParser(description="Resolve encoding-corrupted and orphan unresolved placements.")
    ap.add_argument("--apply", action="store_true",
                    help="Write output files. Default is dry-run (print only).")
    args = ap.parse_args()
    dry_run = not args.apply

    if dry_run:
        print("DRY RUN — pass --apply to write output files.\n")

    truth      = pd.read_csv(TRUTH_IN,      dtype=str).fillna("")
    unresolved = pd.read_csv(UNRESOLVED_IN, dtype=str).fillna("")
    placements = pd.read_csv(PLACEMENTS_IN, dtype=str).fillna("")

    print(f"Loaded Truth:      {len(truth)} rows")
    print(f"Loaded Unresolved: {len(unresolved)} rows")
    print(f"Loaded Placements: {len(placements)} rows")
    print()

    truth_cols   = list(truth.columns)
    truth_canons = set(truth["person_canon"].str.strip())
    new_truth_rows: list[dict] = []
    pbp_changes  = 0

    # Runtime UUID map (will be extended by B and C)
    uuid_map: dict[str, str] = {}

    # -------------------------------------------------------------------------
    # A) UUID_BACKFILL
    # -------------------------------------------------------------------------
    print("=== A) UUID_BACKFILL ===")
    for canon, pid in UUID_BACKFILL.items():
        mask = (placements["person_canon"] == canon) & (placements["person_id"].str.strip() == "")
        n = mask.sum()
        if n == 0:
            print(f"  {canon!r}: no empty rows (already filled)")
            continue
        print(f"  {canon!r}: filling {n} row(s) with {pid[:8]}...")
        if not dry_run:
            placements.loc[mask, "person_id"] = pid
        pbp_changes += n
    print()

    # -------------------------------------------------------------------------
    # B) TRUTH_PROMOTE
    # -------------------------------------------------------------------------
    print("=== B) TRUTH_PROMOTE ===")
    for canon, pid in TRUTH_PROMOTE.items():
        if canon in truth_canons:
            print(f"  SKIP {canon!r} — already in Truth")
            continue
        n_app = (placements["person_id"] == pid).sum()
        row = build_truth_row(truth_cols, pid, canon, "BACKFILL_ORPHAN_v25")
        new_truth_rows.append(row)
        truth_canons.add(canon)
        uuid_map[canon] = pid
        print(f"  PROMOTED: {canon!r}  pid={pid[:8]}  appearances={n_app}")
    print()

    # -------------------------------------------------------------------------
    # C) NEW_PERSONS
    # -------------------------------------------------------------------------
    print("=== C) NEW_PERSONS ===")
    for canon in NEW_PERSONS:
        if canon in truth_canons:
            print(f"  SKIP {canon!r} — already in Truth")
            continue
        pid = make_uuid(canon)
        uuid_map[canon] = pid
        n_pbp = (placements["person_canon"] == canon).sum()
        mask = placements["person_canon"] == canon
        if not dry_run:
            placements.loc[mask, "person_id"] = pid
        pbp_changes += n_pbp
        row = build_truth_row(truth_cols, pid, canon, "NEW_PERSON_v25")
        new_truth_rows.append(row)
        truth_canons.add(canon)
        print(f"  NEW: {canon!r}  pid={pid[:8]}  pbp_rows={n_pbp}")
    print()

    # -------------------------------------------------------------------------
    # D) CANON_CORRECT
    # -------------------------------------------------------------------------
    print("=== D) CANON_CORRECT ===")
    for bad_canon, clean_canon in CANON_CORRECT.items():
        pid = uuid_map.get(clean_canon, "")
        if not pid:
            # Check Truth directly
            truth_match = truth[truth["person_canon"] == clean_canon]
            if len(truth_match):
                pid = truth_match["effective_person_id"].iloc[0]
        if not pid:
            print(f"  ERROR: no UUID for {clean_canon!r} — skipping {bad_canon!r}")
            continue
        mask = placements["person_canon"] == bad_canon
        n = mask.sum()
        if n == 0:
            print(f"  {bad_canon!r}: no PBP rows found")
            continue
        print(f"  {bad_canon!r} → {clean_canon!r}  pid={pid[:8]}  rows={n}")
        if not dry_run:
            placements.loc[mask, "person_canon"] = clean_canon
            placements.loc[mask, "person_id"]    = pid
            if "norm" in placements.columns:
                placements.loc[mask, "norm"] = clean_canon.lower().strip()
        pbp_changes += n
    print()

    # -------------------------------------------------------------------------
    # E) NOISE
    # -------------------------------------------------------------------------
    print("=== E) NOISE ===")
    for canon in sorted(NOISE):
        mask = placements["person_canon"] == canon
        n = mask.sum()
        if n == 0:
            print(f"  {canon!r}: no PBP rows found")
            continue
        print(f"  {canon!r} → __NON_PERSON__  rows={n}")
        if not dry_run:
            placements.loc[mask, "person_canon"] = "__NON_PERSON__"
            placements.loc[mask, "person_id"]    = ""
            if "norm" in placements.columns:
                placements.loc[mask, "norm"] = "__non_person__"
        pbp_changes += n
    print()

    # -------------------------------------------------------------------------
    # Summary
    # -------------------------------------------------------------------------
    n_new = len(new_truth_rows)
    truth_final_len = len(truth) + n_new

    print("=" * 60)
    print(f"Truth:      {len(truth)} → {truth_final_len} (+{n_new} new rows)")
    print(f"Unresolved: {len(unresolved)} → {len(unresolved)} (unchanged)")
    print(f"Placements: {len(placements)} rows ({pbp_changes} values updated)")
    print("=" * 60)

    if dry_run:
        print("\nDRY RUN complete — pass --apply to write output files.")
        return 0

    # Apply Truth additions
    if new_truth_rows:
        new_df = pd.DataFrame(new_truth_rows, columns=truth_cols)
        truth_out = pd.concat([truth, new_df], ignore_index=True)
    else:
        truth_out = truth.copy()

    truth_out.to_csv(TRUTH_OUT, index=False)
    unresolved.to_csv(UNRESOLVED_OUT, index=False)
    placements.to_csv(PLACEMENTS_OUT, index=False)

    print(f"\nWritten: {TRUTH_OUT.name} ({len(truth_out)} rows)")
    print(f"Written: {UNRESOLVED_OUT.name} ({len(unresolved)} rows)")
    print(f"Written: {PLACEMENTS_OUT.name} ({len(placements)} rows)")
    return 0


if __name__ == "__main__":
    sys.exit(main())
